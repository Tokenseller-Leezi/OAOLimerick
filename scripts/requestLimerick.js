const LIMERICK_ADDRESS = "0x940De836ec6Fc83f3327e69Fd7eAe79d64fb5BD5";
const LIMERICK = new web3.eth.Contract(LIMERICK_ABI, LIMERICK_ADDRESS);

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function warpContent(content) {
  return content.split(":\n")[1];
}

async function requestLimerick(_prompt) {
  const idOfWord = await LIMERICK.methods.idOfWord(_prompt).call();
  if(idOfWord != 0) {
    console.log("[+] limerick already exists");
    const limerick = await LIMERICK.methods.limericks(idOfWord).call();
    console.log(warpContent(limerick.content));
    process.exit(0);
  }
  const limerick = await ethers.getContractAt("OAOLimerick", LIMERICK_ADDRESS);
  const result = await limerick.requestLimerick(word, {
    value: web3.utils.toWei("0.005", "ether"),
  });
  console.log("https://sepolia.etherscan.io/tx/" + result.hash);
}

async function getLimerick() {
  console.log("[+] get limerick");
  const currentId = await LIMERICK.methods.currentId().call();
  const limerick = await LIMERICK.methods.limericks(currentId).call();
  console.log(warpContent(limerick.content));
}

async function main() {
  await requestLimerick(word);
  console.log("[.] wait for next block commit...");
  await sleep(40000);
  await getLimerick();
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});